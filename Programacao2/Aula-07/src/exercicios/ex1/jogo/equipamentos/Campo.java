package exercicios.ex1.jogo.equipamentos;

public class Campo {
	
	private int larg;
	private int compr;
	
	public Campo() {
		super();
	}

	public int getLarg() {
		return larg;
	}

	public void setLarg(int larg) {
		this.larg = larg;
	}

	public int getCompr() {
		return compr;
	}

	public void setCompr(int compr) {
		this.compr = compr;
	}
}
